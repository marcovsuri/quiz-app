package org.roxburylatin.advcompsci.quizapp.application;

/**
 * The Request enum represents the different types of requests that can be made in the quiz
 * application. It includes three types: {@code REQUEST_QUIZ}, {@code ASK_FOR_HELP}, and {@code
 * SUBMIT_QUIZ}. Each request type corresponds to a specific action that a student can take within
 * the application.
 */
public enum Request {
  REQUEST_QUIZ,
  ASK_FOR_HELP,
  SUBMIT_QUIZ
}
